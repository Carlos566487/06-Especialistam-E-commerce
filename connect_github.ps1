# Script de Conexão com o GitHub
# Execute estes comandos um por um no seu terminal PowerShell ou Bash

# 1. Adicionar o repositório remoto
git remote add origin https://github.com/Carlos566487/06-Especialistam-E-commerce.git

# 2. Definir a branch principal como 'main'
git branch -M main

# 3. Fazer o push inicial
# (Será solicitado login no GitHub se não estiver autenticado)
git push -u origin main
